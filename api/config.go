package bot

import (
	"log"
	"os"

	"github.com/joho/godotenv"
)

func envTwilioAccountSID() string {
	err := godotenv.Load(".env")
	if err != nil {
		log.Fatalln(err)
		log.Fatal("Error loading .env file")
	}
	return os.Getenv("TWILIO_ACCOUNT_SID")
}

